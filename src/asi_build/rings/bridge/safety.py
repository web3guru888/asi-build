"""
Bridge Safety System
=====================

Production-grade circuit breakers, anomaly detection, rate-limit
monitoring, and validator health tracking for the Rings ↔ Ethereum
bridge.

Components
~~~~~~~~~~

- :class:`CircuitBreaker` — configurable CLOSED → OPEN → HALF_OPEN
  state machine with sliding-window error rate and consecutive-failure
  thresholds.
- :class:`AnomalyDetector` — EWMA-based statistical anomaly detection
  for deposit/withdrawal volume, frequency, and value outliers.
- :class:`RateLimitMonitor` — tracks daily limit utilisation and
  predicts exhaustion.
- :class:`ValidatorHealthMonitor` — heartbeat liveness and attestation
  success rate tracking.
- :class:`BridgeSafetyManager` — orchestrates all safety sub-systems
  and can auto-pause the bridge contract on critical alerts.

No external ML dependencies — all anomaly detection uses exponentially
weighted moving averages and z-score thresholds (pure statistics).
"""

from __future__ import annotations

import logging
import math
import time
from collections import deque
from dataclasses import dataclass, field
from enum import Enum
from typing import Any, Callable, Deque, Dict, List, Optional

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Alert types
# ---------------------------------------------------------------------------


class AlertSeverity(Enum):
    """Severity levels for safety alerts."""

    INFO = "info"
    WARNING = "warning"
    CRITICAL = "critical"
    EMERGENCY = "emergency"


@dataclass
class SafetyAlert:
    """An alert generated by the monitoring system.

    Attributes
    ----------
    timestamp : float
        Unix timestamp when the alert was created.
    severity : AlertSeverity
        How severe the condition is.
    source : str
        Which monitor generated this alert (e.g. ``"circuit_breaker"``).
    message : str
        Human-readable description.
    metrics : dict
        Any numeric context (e.g. z-score, error rate).
    acknowledged : bool
        Whether an operator has ack'd this alert.
    """

    timestamp: float
    severity: AlertSeverity
    source: str
    message: str
    metrics: dict = field(default_factory=dict)
    acknowledged: bool = False


