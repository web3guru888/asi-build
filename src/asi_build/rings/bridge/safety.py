class AnomalyDetector:
    """Detects anomalous bridge activity using statistical methods.

    Monitors:

    1. **Volume anomalies** — sudden spikes in deposit/withdrawal
       amounts that are statistical outliers.
    2. **Frequency anomalies** — unusually short inter-transaction
       intervals.
    3. **Failed-attempt patterns** — bursts of failures suggesting an
       attack.

    Uses exponentially weighted moving average (EWMA) and standard
    deviation for anomaly detection.  No ML dependencies — pure
    statistics.

    Parameters
    ----------
    volume_window : int
        Rolling window size for amount history.
    z_score_threshold : float
        Number of standard deviations for an outlier.
    ewma_alpha : float
        Smoothing factor for EWMA (0 < α ≤ 1).
    min_samples : int
        Minimum number of observations before alerting.
    """

    def __init__(
        self,
        volume_window: int = 100,
        z_score_threshold: float = 3.0,
        ewma_alpha: float = 0.1,
        min_samples: int = 10,
    ) -> None:
        self.volume_window = volume_window
        self.z_score_threshold = z_score_threshold
        self.ewma_alpha = ewma_alpha
        self.min_samples = min_samples

        # Amount history
        self._deposit_amounts: Deque[float] = deque(maxlen=volume_window)
        self._withdrawal_amounts: Deque[float] = deque(maxlen=volume_window)

        # Frequency tracking (deposit intervals)
        self._deposit_intervals: Deque[float] = deque(maxlen=volume_window)
        self._last_deposit_time: float = 0.0

        # EWMA state for deposits
        self._ewma_deposit: float = 0.0
        self._ewma_variance: float = 0.0
        self._deposit_count: int = 0

        # EWMA state for withdrawals
        self._ewma_withdrawal: float = 0.0
        self._ewma_withdrawal_variance: float = 0.0
        self._withdrawal_count: int = 0

        # EWMA state for deposit intervals
        self._ewma_interval: float = 0.0
        self._ewma_interval_variance: float = 0.0

        # Failed attempt tracking
        self._recent_failures: Deque[float] = deque(maxlen=volume_window)
        self._failure_burst_threshold: int = 5  # failures in 60s

        # All generated alerts
        self._alerts: List[SafetyAlert] = []

    # ── Public API ───────────────────────────────────────────────────────

    def observe_deposit(
        self,
        amount: int,
        sender: str,
        timestamp: Optional[float] = None,
    ) -> Optional[SafetyAlert]:
        """Record a deposit and check for anomalies.

        Parameters
        ----------
        amount : int
            Deposit amount in wei.
        sender : str
            Sender Ethereum address.
        timestamp : float, optional
            Observation time (defaults to ``time.time()``).

        Returns
        -------
        SafetyAlert or None
            An alert if the deposit is anomalous.
        """
        ts = timestamp if timestamp is not None else time.time()
        amt = float(amount)

        # Track frequency (check BEFORE updating EWMA)
        interval: Optional[float] = None
        if self._last_deposit_time > 0:
            interval = ts - self._last_deposit_time
        self._last_deposit_time = ts

        # Snapshot EWMA state BEFORE updating (so the outlier doesn't
        # pollute the baseline we compare against)
        prev_mean = self._ewma_deposit
        prev_var = self._ewma_variance
        prev_count = self._deposit_count

        # Now update tracking
        self._deposit_amounts.append(amt)
        self._deposit_count += 1
        self._update_ewma_deposit(amt)

        if interval is not None:
            self._deposit_intervals.append(interval)
            # Snapshot interval EWMA before update
            prev_interval_mean = self._ewma_interval
            prev_interval_var = self._ewma_interval_variance
            self._update_ewma_interval(interval)
        else:
            prev_interval_mean = self._ewma_interval
            prev_interval_var = self._ewma_interval_variance

        # Check volume anomaly against the pre-update baseline
        alert = self._check_volume_anomaly(
            amt, self._deposit_amounts,
            prev_mean, prev_var,
            prev_count, "deposit",
        )
        if alert is not None:
            alert.metrics["sender"] = sender
            self._alerts.append(alert)
            return alert

        # Check frequency anomaly (using pre-update interval EWMA)
        if interval is not None:
            freq_alert = self._check_frequency_anomaly_with(
                interval, ts, prev_interval_mean, prev_interval_var,
            )
            if freq_alert is not None:
                freq_alert.metrics["sender"] = sender
                self._alerts.append(freq_alert)
                return freq_alert

        return None

    def observe_withdrawal(
        self,
        amount: int,
        recipient: str,
        timestamp: Optional[float] = None,
    ) -> Optional[SafetyAlert]:
        """Record a withdrawal and check for anomalies.

        Parameters
        ----------
        amount : int
            Withdrawal amount in wei.
        recipient : str
            Recipient Ethereum address.
        timestamp : float, optional
            Observation time.

        Returns
        -------
        SafetyAlert or None
        """
        ts = timestamp if timestamp is not None else time.time()
        amt = float(amount)

        # Snapshot BEFORE update
        prev_mean = self._ewma_withdrawal
        prev_var = self._ewma_withdrawal_variance
        prev_count = self._withdrawal_count

        self._withdrawal_amounts.append(amt)
        self._withdrawal_count += 1
        self._update_ewma_withdrawal(amt)

        alert = self._check_volume_anomaly(
            amt, self._withdrawal_amounts,
            prev_mean, prev_var,
            prev_count, "withdrawal",
        )
        if alert is not None:
            alert.metrics["recipient"] = recipient
            self._alerts.append(alert)
            return alert

        return None

    def observe_failed_attempt(
        self,
        operation: str,
        error: str,
        timestamp: Optional[float] = None,
    ) -> Optional[SafetyAlert]:
        """Record a failed operation and check for attack patterns.

        Generates an alert if more than *_failure_burst_threshold*
        failures have occurred in the last 60 seconds.

        Parameters
        ----------
        operation : str
            E.g. ``"deposit"`` or ``"withdrawal"``.
        error : str
            Error description.
        timestamp : float, optional
            Observation time.

        Returns
        -------
        SafetyAlert or None
        """
        ts = timestamp if timestamp is not None else time.time()
        self._recent_failures.append(ts)

        # Count failures in the last 60 seconds
        cutoff = ts - 60.0
        recent = sum(1 for t in self._recent_failures if t >= cutoff)

        if recent >= self._failure_burst_threshold:
            severity = (
                AlertSeverity.CRITICAL if recent >= self._failure_burst_threshold * 2
                else AlertSeverity.WARNING
            )
            alert = SafetyAlert(
                timestamp=ts,
                severity=severity,
                source="anomaly_detector",
                message=(
                    f"Failure burst detected: {recent} failures in 60s "
                    f"(operation={operation}, latest error: {error})"
                ),
                metrics={
                    "recent_failures": recent,
                    "operation": operation,
                    "burst_threshold": self._failure_burst_threshold,
                },
            )
            self._alerts.append(alert)
            return alert

        return None

    # ── Properties ───────────────────────────────────────────────────────

    @property
    def stats(self) -> dict:
        """Current detector statistics."""
        return {
            "deposit_count": self._deposit_count,
            "withdrawal_count": self._withdrawal_count,
            "ewma_deposit": round(self._ewma_deposit, 4),
            "ewma_deposit_stddev": round(math.sqrt(max(self._ewma_variance, 0)), 4),
            "ewma_withdrawal": round(self._ewma_withdrawal, 4),
            "ewma_withdrawal_stddev": round(
                math.sqrt(max(self._ewma_withdrawal_variance, 0)), 4,
            ),
            "ewma_interval": round(self._ewma_interval, 4),
            "alerts_generated": len(self._alerts),
        }

    # ── Internal helpers ─────────────────────────────────────────────────

    def _update_ewma_deposit(self, value: float) -> None:
        """Update EWMA mean and variance for deposits."""
        alpha = self.ewma_alpha
        if self._deposit_count == 1:
            self._ewma_deposit = value
            self._ewma_variance = 0.0
        else:
            diff = value - self._ewma_deposit
            self._ewma_deposit += alpha * diff
            self._ewma_variance = (1 - alpha) * (self._ewma_variance + alpha * diff * diff)

    def _update_ewma_withdrawal(self, value: float) -> None:
        """Update EWMA mean and variance for withdrawals."""
        alpha = self.ewma_alpha
        if self._withdrawal_count == 1:
            self._ewma_withdrawal = value
            self._ewma_withdrawal_variance = 0.0
        else:
            diff = value - self._ewma_withdrawal
            self._ewma_withdrawal += alpha * diff
            self._ewma_withdrawal_variance = (1 - alpha) * (
                self._ewma_withdrawal_variance + alpha * diff * diff
            )

    def _update_ewma_interval(self, value: float) -> None:
        """Update EWMA for inter-deposit intervals."""
        alpha = self.ewma_alpha
        if len(self._deposit_intervals) == 1:
            self._ewma_interval = value
            self._ewma_interval_variance = 0.0
        else:
            diff = value - self._ewma_interval
            self._ewma_interval += alpha * diff
            self._ewma_interval_variance = (1 - alpha) * (
                self._ewma_interval_variance + alpha * diff * diff
            )

    def _check_volume_anomaly(
        self,
        amount: float,
        history: Deque[float],
        ewma_mean: float,
        ewma_var: float,
        count: int,
        label: str,
    ) -> Optional[SafetyAlert]:
        """Check if *amount* is a statistical outlier using EWMA + z-score."""
        if count < self.min_samples:
            return None

        stddev = math.sqrt(max(ewma_var, 0))
        if stddev < 1e-12:
            # All values identical — no variance to detect anomalies against
            return None

        z_score = (amount - ewma_mean) / stddev

        if abs(z_score) >= self.z_score_threshold:
            severity = (
                AlertSeverity.CRITICAL
                if abs(z_score) >= self.z_score_threshold * 2
                else AlertSeverity.WARNING
            )
            alert = SafetyAlert(
                timestamp=time.time(),
                severity=severity,
                source="anomaly_detector",
                message=(
                    f"Volume anomaly on {label}: amount={amount:.0f}, "
                    f"z-score={z_score:.2f} (threshold=±{self.z_score_threshold})"
                ),
                metrics={
                    "amount": amount,
                    "z_score": round(z_score, 4),
                    "ewma_mean": round(ewma_mean, 4),
                    "ewma_stddev": round(stddev, 4),
                    "label": label,
                },
            )
            return alert

        return None

    def _check_frequency_anomaly_with(
        self,
        interval: float,
        timestamp: float,
        ewma_mean: float,
        ewma_var: float,
    ) -> Optional[SafetyAlert]:
        """Check if the inter-deposit interval is anomalously short.

        Uses *pre-update* EWMA statistics so the current observation
        doesn't pollute the baseline.
        """
        if len(self._deposit_intervals) < self.min_samples:
            return None

        stddev = math.sqrt(max(ewma_var, 0))
        if stddev < 1e-12:
            return None

        # A *negative* z-score means the interval is shorter than expected
        z_score = (interval - ewma_mean) / stddev

        if z_score <= -self.z_score_threshold:
            severity = (
                AlertSeverity.CRITICAL
                if z_score <= -self.z_score_threshold * 2
                else AlertSeverity.WARNING
            )
            alert = SafetyAlert(
                timestamp=timestamp,
                severity=severity,
                source="anomaly_detector",
                message=(
                    f"Frequency anomaly: interval={interval:.3f}s, "
                    f"z-score={z_score:.2f} (mean={self._ewma_interval:.3f}s)"
                ),
                metrics={
                    "interval": round(interval, 4),
                    "z_score": round(z_score, 4),
                    "ewma_interval": round(self._ewma_interval, 4),
                    "ewma_interval_stddev": round(stddev, 4),
                },
            )
            return alert

        return None

    def __repr__(self) -> str:
        return (
            f"AnomalyDetector(deposits={self._deposit_count}, "
            f"withdrawals={self._withdrawal_count}, "
            f"alerts={len(self._alerts)})"
        )


