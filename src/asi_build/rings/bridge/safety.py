class RateLimitMonitor:
    """Monitors rate-limit utilisation and predicts exhaustion.

    Generates warnings when:

    - Daily limit is > *warning_threshold* (default 80%) utilised.
    - Current velocity would exhaust the limit before the daily reset.
    - Per-tx limit is being consistently hit (potential DoS).

    Parameters
    ----------
    daily_limit : int
        Maximum daily bridge throughput in wei.
    per_tx_limit : int
        Maximum per-transaction amount in wei.
    warning_threshold : float
        Fraction of daily limit that triggers a warning (0.0–1.0).
    """

    def __init__(
        self,
        daily_limit: int,
        per_tx_limit: int,
        warning_threshold: float = 0.8,
    ) -> None:
        self.daily_limit = daily_limit
        self.per_tx_limit = per_tx_limit
        self.warning_threshold = warning_threshold

        self._daily_used: int = 0
        self._tx_count: int = 0
        self._per_tx_hits: int = 0  # times someone hit the per-tx cap
        self._day_start: float = time.time()
        self._tx_timestamps: Deque[float] = deque(maxlen=1000)
        self._alerts: List[SafetyAlert] = []

    # ── Public API ───────────────────────────────────────────────────────

    def record_transaction(self, amount: int) -> Optional[SafetyAlert]:
        """Record a bridge transaction and check utilisation.

        Parameters
        ----------
        amount : int
            Transaction amount in wei.

        Returns
        -------
        SafetyAlert or None
            An alert if a warning threshold is crossed.
        """
        now = time.time()
        self._daily_used += amount
        self._tx_count += 1
        self._tx_timestamps.append(now)

        if amount >= self.per_tx_limit:
            self._per_tx_hits += 1

        # Check utilisation
        if self.daily_limit > 0:
            utilisation = self._daily_used / self.daily_limit
        else:
            utilisation = 1.0 if self._daily_used > 0 else 0.0

        alert: Optional[SafetyAlert] = None

        if utilisation >= 1.0:
            alert = SafetyAlert(
                timestamp=now,
                severity=AlertSeverity.CRITICAL,
                source="rate_limit_monitor",
                message=(
                    f"Daily limit EXHAUSTED: used {self._daily_used} / {self.daily_limit} "
                    f"({utilisation:.1%})"
                ),
                metrics={
                    "daily_used": self._daily_used,
                    "daily_limit": self.daily_limit,
                    "utilisation": round(utilisation, 4),
                },
            )
        elif utilisation >= self.warning_threshold:
            severity = (
                AlertSeverity.CRITICAL if utilisation >= 0.95
                else AlertSeverity.WARNING
            )
            exhaustion = self.predict_exhaustion()
            alert = SafetyAlert(
                timestamp=now,
                severity=severity,
                source="rate_limit_monitor",
                message=(
                    f"Rate limit warning: {utilisation:.1%} of daily limit used "
                    f"({self._daily_used} / {self.daily_limit})"
                    + (
                        f", predicted exhaustion in {exhaustion:.0f}s"
                        if exhaustion is not None
                        else ""
                    )
                ),
                metrics={
                    "daily_used": self._daily_used,
                    "daily_limit": self.daily_limit,
                    "utilisation": round(utilisation, 4),
                    "predicted_exhaustion_seconds": exhaustion,
                },
            )

        if alert is not None:
            self._alerts.append(alert)

        return alert

    def predict_exhaustion(self) -> Optional[float]:
        """Predict when the daily limit will be exhausted.

        Uses the average transaction rate (amount / time) to project
        when remaining capacity will be consumed.

        Returns
        -------
        float or None
            Seconds from now until exhaustion, or ``None`` if no
            prediction can be made (e.g. no transactions yet or limit
            already reached).
        """
        if self.daily_limit <= 0:
            return 0.0

        remaining = self.daily_limit - self._daily_used
        if remaining <= 0:
            return 0.0

        now = time.time()
        elapsed = now - self._day_start
        if elapsed <= 0 or self._daily_used <= 0:
            return None

        rate = self._daily_used / elapsed  # wei per second
        if rate <= 0:
            return None

        return remaining / rate

    def reset_daily(self) -> None:
        """Called when the daily limit resets (new 24h window)."""
        self._daily_used = 0
        self._tx_count = 0
        self._per_tx_hits = 0
        self._day_start = time.time()
        self._tx_timestamps.clear()

    @property
    def stats(self) -> dict:
        """Current rate-limit statistics."""
        utilisation = 0.0
        if self.daily_limit > 0:
            utilisation = self._daily_used / self.daily_limit
        return {
            "daily_used": self._daily_used,
            "daily_limit": self.daily_limit,
            "per_tx_limit": self.per_tx_limit,
            "utilisation": round(utilisation, 4),
            "tx_count": self._tx_count,
            "per_tx_hits": self._per_tx_hits,
            "alerts_generated": len(self._alerts),
        }

    def __repr__(self) -> str:
        return (
            f"RateLimitMonitor(used={self._daily_used}/{self.daily_limit}, "
            f"txs={self._tx_count})"
        )


