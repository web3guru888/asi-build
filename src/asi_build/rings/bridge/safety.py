class ValidatorHealthMonitor:
    """Monitors validator liveness and attestation health.

    Generates alerts when:

    - A validator hasn't sent a heartbeat within the expected interval
      multiplied by *heartbeat_timeout_multiplier*.
    - The attestation success rate of a validator drops below 50%.
    - The number of active validators drops below *threshold*.

    Parameters
    ----------
    expected_validators : int
        Expected total number of validators (for quorum checks).
    threshold : int
        Minimum number of active validators needed.
    heartbeat_interval : float
        Expected seconds between heartbeats.
    heartbeat_timeout_multiplier : float
        Factor applied to *heartbeat_interval* to determine timeout.
    """

    def __init__(
        self,
        expected_validators: int = 6,
        threshold: int = 4,
        heartbeat_interval: float = 30.0,
        heartbeat_timeout_multiplier: float = 3.0,
    ) -> None:
        self.expected_validators = expected_validators
        self.threshold = threshold
        self.heartbeat_interval = heartbeat_interval
        self.heartbeat_timeout = heartbeat_interval * heartbeat_timeout_multiplier

        self._last_heartbeats: Dict[str, float] = {}
        self._attestation_results: Dict[str, Deque[bool]] = {}
        self._alerts: List[SafetyAlert] = []

    # ── Public API ───────────────────────────────────────────────────────

    def record_heartbeat(self, validator_did: str) -> None:
        """Record a heartbeat from a validator.

        Parameters
        ----------
        validator_did : str
            The DID of the validator.
        """
        self._last_heartbeats[validator_did] = time.time()
        # Ensure an attestation deque exists
        if validator_did not in self._attestation_results:
            self._attestation_results[validator_did] = deque(maxlen=100)

    def record_attestation(self, validator_did: str, success: bool) -> None:
        """Record an attestation result for a validator.

        Parameters
        ----------
        validator_did : str
            The DID of the validator.
        success : bool
            Whether the attestation was successful.
        """
        if validator_did not in self._attestation_results:
            self._attestation_results[validator_did] = deque(maxlen=100)
        self._attestation_results[validator_did].append(success)

    def check_health(self) -> List[SafetyAlert]:
        """Check all validators and return any alerts.

        This should be called periodically (e.g. every heartbeat interval).

        Returns
        -------
        list of SafetyAlert
            Zero or more alerts describing validator health issues.
        """
        now = time.time()
        alerts: List[SafetyAlert] = []

        # Check individual validator heartbeats
        for did, last_hb in self._last_heartbeats.items():
            elapsed = now - last_hb
            if elapsed > self.heartbeat_timeout:
                alert = SafetyAlert(
                    timestamp=now,
                    severity=AlertSeverity.WARNING,
                    source="validator_health",
                    message=(
                        f"Validator {did} heartbeat timeout: "
                        f"{elapsed:.1f}s since last heartbeat "
                        f"(timeout={self.heartbeat_timeout:.1f}s)"
                    ),
                    metrics={
                        "validator_did": did,
                        "elapsed": round(elapsed, 1),
                        "timeout": self.heartbeat_timeout,
                    },
                )
                alerts.append(alert)

        # Check attestation success rates
        for did, results in self._attestation_results.items():
            if len(results) < 5:
                continue
            success_rate = sum(1 for r in results if r) / len(results)
            if success_rate < 0.5:
                alert = SafetyAlert(
                    timestamp=now,
                    severity=AlertSeverity.WARNING,
                    source="validator_health",
                    message=(
                        f"Validator {did} low attestation success rate: "
                        f"{success_rate:.1%} ({sum(1 for r in results if r)}/{len(results)})"
                    ),
                    metrics={
                        "validator_did": did,
                        "success_rate": round(success_rate, 4),
                        "total_attestations": len(results),
                    },
                )
                alerts.append(alert)

        # Check validator count
        active = self.active_validators
        if active < self.threshold:
            severity = (
                AlertSeverity.EMERGENCY if active == 0
                else AlertSeverity.CRITICAL
            )
            alert = SafetyAlert(
                timestamp=now,
                severity=severity,
                source="validator_health",
                message=(
                    f"Active validators below threshold: "
                    f"{active}/{self.expected_validators} "
                    f"(threshold={self.threshold})"
                ),
                metrics={
                    "active_validators": active,
                    "expected": self.expected_validators,
                    "threshold": self.threshold,
                },
            )
            alerts.append(alert)

        self._alerts.extend(alerts)
        return alerts

    # ── Properties ───────────────────────────────────────────────────────

    @property
    def active_validators(self) -> int:
        """Number of validators with recent heartbeats (within timeout)."""
        now = time.time()
        return sum(
            1 for t in self._last_heartbeats.values()
            if (now - t) <= self.heartbeat_timeout
        )

    @property
    def stats(self) -> dict:
        """Current validator-health statistics."""
        return {
            "known_validators": len(self._last_heartbeats),
            "active_validators": self.active_validators,
            "expected_validators": self.expected_validators,
            "threshold": self.threshold,
            "alerts_generated": len(self._alerts),
        }

    def __repr__(self) -> str:
        return (
            f"ValidatorHealthMonitor(active={self.active_validators}/"
            f"{self.expected_validators}, threshold={self.threshold})"
        )


