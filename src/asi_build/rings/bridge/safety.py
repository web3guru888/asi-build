class BridgeSafetyManager:
    """Orchestrates all safety components for the bridge.

    Ties together circuit breakers, anomaly detection, rate-limit
    monitoring, and validator health into a single safety layer.

    When critical / emergency alerts are generated and *auto_pause_enabled*
    is ``True``, the manager will call ``contract_client.pause()`` to
    halt the on-chain bridge.

    Parameters
    ----------
    contract_client : object, optional
        A :class:`BridgeContractClient` (or duck-typed equivalent with an
        async ``pause()`` method).
    validator : object, optional
        A :class:`BridgeValidator` (or duck-typed equivalent with
        ``emergency_halt(reason)``).
    daily_limit : int
        Daily bridge throughput limit in wei.
    per_tx_limit : int
        Per-transaction bridge limit in wei.
    auto_pause_enabled : bool
        Whether to automatically pause the contract on EMERGENCY alerts.
    """

    def __init__(
        self,
        contract_client: Any = None,
        validator: Any = None,
        daily_limit: int = 100 * 10**18,
        per_tx_limit: int = 10 * 10**18,
        auto_pause_enabled: bool = True,
    ) -> None:
        self.contract_client = contract_client
        self.validator = validator

        # Circuit breakers
        self.deposit_breaker = CircuitBreaker(
            "deposit", failure_threshold=3, cooldown_seconds=120,
        )
        self.withdrawal_breaker = CircuitBreaker(
            "withdrawal", failure_threshold=3, cooldown_seconds=120,
        )
        self.sync_breaker = CircuitBreaker(
            "sync_committee", failure_threshold=2, cooldown_seconds=300,
        )

        # Sub-monitors
        self.anomaly_detector = AnomalyDetector()
        self.rate_monitor = RateLimitMonitor(daily_limit, per_tx_limit)
        self.validator_monitor = ValidatorHealthMonitor()

        # Alert aggregation
        self._alerts: List[SafetyAlert] = []
        self._auto_pause_enabled = auto_pause_enabled
        self._paused_by_safety: bool = False

    # ── Pre-flight checks ────────────────────────────────────────────────

    async def check_deposit(self, amount: int, sender: str) -> bool:
        """Pre-flight check before processing a deposit.

        Returns ``True`` if safe to proceed, ``False`` if the operation
        should be blocked.
        """
        # Circuit breaker gate
        if not self.deposit_breaker.is_available():
            logger.warning("Deposit blocked: circuit breaker OPEN")
            return False

        # Anomaly check
        alert = self.anomaly_detector.observe_deposit(amount, sender)
        if alert is not None:
            await self._handle_alert(alert)
            if alert.severity in (AlertSeverity.CRITICAL, AlertSeverity.EMERGENCY):
                return False

        # Rate-limit check
        rate_alert = self.rate_monitor.record_transaction(amount)
        if rate_alert is not None:
            await self._handle_alert(rate_alert)
            if rate_alert.severity == AlertSeverity.CRITICAL:
                return False

        return True

    async def check_withdrawal(self, amount: int, recipient: str) -> bool:
        """Pre-flight check before processing a withdrawal.

        Returns ``True`` if safe to proceed.
        """
        # Circuit breaker gate
        if not self.withdrawal_breaker.is_available():
            logger.warning("Withdrawal blocked: circuit breaker OPEN")
            return False

        # Anomaly check
        alert = self.anomaly_detector.observe_withdrawal(amount, recipient)
        if alert is not None:
            await self._handle_alert(alert)
            if alert.severity in (AlertSeverity.CRITICAL, AlertSeverity.EMERGENCY):
                return False

        # Rate-limit check
        rate_alert = self.rate_monitor.record_transaction(amount)
        if rate_alert is not None:
            await self._handle_alert(rate_alert)
            if rate_alert.severity == AlertSeverity.CRITICAL:
                return False

        return True

    # ── Result recording ─────────────────────────────────────────────────

    def record_deposit_result(
        self, success: bool, amount: int = 0, sender: str = "",
    ) -> None:
        """Record the outcome of a deposit operation."""
        if success:
            self.deposit_breaker.record_success()
        else:
            self.deposit_breaker.record_failure()
            self.anomaly_detector.observe_failed_attempt(
                "deposit", f"deposit failed (amount={amount}, sender={sender})",
            )

    def record_withdrawal_result(
        self, success: bool, amount: int = 0, recipient: str = "",
    ) -> None:
        """Record the outcome of a withdrawal operation."""
        if success:
            self.withdrawal_breaker.record_success()
        else:
            self.withdrawal_breaker.record_failure()
            self.anomaly_detector.observe_failed_attempt(
                "withdrawal",
                f"withdrawal failed (amount={amount}, recipient={recipient})",
            )

    # ── Alert handling ───────────────────────────────────────────────────

    async def _handle_alert(self, alert: SafetyAlert) -> None:
        """Process an alert — may trigger auto-pause on EMERGENCY."""
        self._alerts.append(alert)
        logger.log(
            logging.CRITICAL if alert.severity == AlertSeverity.EMERGENCY
            else logging.WARNING,
            "Safety alert [%s/%s]: %s",
            alert.severity.value, alert.source, alert.message,
        )

        if (
            alert.severity == AlertSeverity.EMERGENCY
            and self._auto_pause_enabled
        ):
            await self._auto_pause(alert.message)

    async def _auto_pause(self, reason: str) -> None:
        """Auto-pause the bridge contract and/or trigger emergency halt."""
        if self._paused_by_safety:
            return  # already paused

        logger.critical("AUTO-PAUSE triggered: %s", reason)
        self._paused_by_safety = True

        # Pause on-chain contract
        if self.contract_client is not None:
            try:
                await self.contract_client.pause()
                logger.critical("Bridge contract paused by safety system")
            except Exception as exc:
                logger.error("Failed to pause contract: %s", exc)

        # Halt the validator
        if self.validator is not None:
            try:
                await self.validator.emergency_halt(
                    f"Auto-halt by safety system: {reason}"
                )
            except Exception as exc:
                logger.error("Failed to emergency halt validator: %s", exc)

    # ── Query ────────────────────────────────────────────────────────────

    def get_alerts(
        self,
        since: float = 0,
        severity: Optional[AlertSeverity] = None,
    ) -> List[SafetyAlert]:
        """Get alerts, optionally filtered by time and severity.

        Parameters
        ----------
        since : float
            Only return alerts after this Unix timestamp.
        severity : AlertSeverity, optional
            Only return alerts of this severity.

        Returns
        -------
        list of SafetyAlert
        """
        alerts = self._alerts
        if since > 0:
            alerts = [a for a in alerts if a.timestamp >= since]
        if severity is not None:
            alerts = [a for a in alerts if a.severity == severity]
        return alerts

    @property
    def health_report(self) -> dict:
        """Comprehensive safety health report."""
        return {
            "paused_by_safety": self._paused_by_safety,
            "auto_pause_enabled": self._auto_pause_enabled,
            "circuit_breakers": {
                "deposit": self.deposit_breaker.stats,
                "withdrawal": self.withdrawal_breaker.stats,
                "sync_committee": self.sync_breaker.stats,
            },
            "anomaly_detector": self.anomaly_detector.stats,
            "rate_limit": self.rate_monitor.stats,
            "validator_health": self.validator_monitor.stats,
            "total_alerts": len(self._alerts),
        }

    def reset_all(self) -> None:
        """Reset all monitors and breakers (for testing or recovery)."""
        self.deposit_breaker.reset()
        self.withdrawal_breaker.reset()
        self.sync_breaker.reset()
        self.anomaly_detector = AnomalyDetector()
        self.rate_monitor = RateLimitMonitor(
            self.rate_monitor.daily_limit,
            self.rate_monitor.per_tx_limit,
        )
        self.validator_monitor = ValidatorHealthMonitor()
        self._alerts.clear()
        self._paused_by_safety = False

    def __repr__(self) -> str:
        return (
            f"BridgeSafetyManager("
            f"paused={self._paused_by_safety}, "
            f"alerts={len(self._alerts)}, "
            f"breakers=[{self.deposit_breaker.state.value}/"
            f"{self.withdrawal_breaker.state.value}/"
            f"{self.sync_breaker.state.value}])"
        )
