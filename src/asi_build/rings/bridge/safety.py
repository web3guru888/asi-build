# CircuitBreaker
# ---------------------------------------------------------------------------


class CircuitBreaker:
    """Production circuit breaker with configurable thresholds.

    States: CLOSED (normal) → OPEN (tripped) → HALF_OPEN (testing recovery)

    Trips on:
    - Consecutive failure count exceeding *failure_threshold*.
    - Error rate exceeding *error_rate_threshold* in a sliding window
      of *window_size* results.
    - Manual :meth:`trip` call.

    Recovery:
    - After *cooldown_seconds*, transitions to HALF_OPEN.
    - In HALF_OPEN, the next :meth:`record_success` → CLOSED.
    - In HALF_OPEN, the next :meth:`record_failure` → OPEN (resets
      cooldown timer).
    """

    class State(Enum):
        CLOSED = "closed"
        OPEN = "open"
        HALF_OPEN = "half_open"

    def __init__(
        self,
        name: str,
        failure_threshold: int = 5,
        error_rate_threshold: float = 0.5,
        window_size: int = 20,
        cooldown_seconds: float = 60.0,
        on_trip: Optional[Callable] = None,
        on_reset: Optional[Callable] = None,
    ) -> None:
        self.name = name
        self.failure_threshold = failure_threshold
        self.error_rate_threshold = error_rate_threshold
        self.window_size = window_size
        self.cooldown_seconds = cooldown_seconds
        self.on_trip = on_trip
        self.on_reset = on_reset

        self.state = self.State.CLOSED
        self._consecutive_failures: int = 0
        self._results: Deque[bool] = deque(maxlen=window_size)
        self._last_failure_time: float = 0.0
        self._trip_count: int = 0

    # ── Recording ────────────────────────────────────────────────────────

    def record_success(self) -> None:
        """Record a successful operation.

        If the breaker is in HALF_OPEN, this resets it to CLOSED.
        """
        self._results.append(True)
        self._consecutive_failures = 0

        if self.state == self.State.HALF_OPEN:
            self._do_reset()

    def record_failure(self) -> None:
        """Record a failed operation.  May trip the breaker."""
        self._results.append(False)
        self._consecutive_failures += 1
        self._last_failure_time = time.time()

        if self.state == self.State.HALF_OPEN:
            # Probe failed — back to OPEN
            self._do_trip("half_open probe failed")
            return

        if self.state == self.State.CLOSED:
            # Check consecutive failures
            if self._consecutive_failures >= self.failure_threshold:
                self._do_trip(
                    f"consecutive failures ({self._consecutive_failures}) "
                    f">= threshold ({self.failure_threshold})"
                )
                return

            # Check sliding-window error rate
            if len(self._results) >= self.failure_threshold and self.error_rate > self.error_rate_threshold:
                self._do_trip(
                    f"error rate ({self.error_rate:.2%}) "
                    f"> threshold ({self.error_rate_threshold:.2%})"
                )

    # ── Availability ─────────────────────────────────────────────────────

    def is_available(self) -> bool:
        """Check whether operations should be allowed through.

        Returns ``True`` for CLOSED and HALF_OPEN states.  For OPEN,
        checks whether the cooldown has elapsed and auto-transitions to
        HALF_OPEN if so.
        """
        if self.state == self.State.CLOSED:
            return True

        if self.state == self.State.HALF_OPEN:
            return True

        # OPEN — check cooldown
        elapsed = time.time() - self._last_failure_time
        if elapsed >= self.cooldown_seconds:
            self.state = self.State.HALF_OPEN
            logger.info(
                "CircuitBreaker[%s]: OPEN → HALF_OPEN after %.1fs cooldown",
                self.name, elapsed,
            )
            return True

        return False

    # ── Manual controls ──────────────────────────────────────────────────

    def trip(self, reason: str = "manual") -> None:
        """Manually trip the breaker to OPEN state."""
        self._do_trip(reason)

    def reset(self) -> None:
        """Manually reset the breaker to CLOSED state."""
        self._do_reset()

    # ── Properties ───────────────────────────────────────────────────────

    @property
    def error_rate(self) -> float:
        """Current error rate in the sliding window (0.0–1.0)."""
        if not self._results:
            return 0.0
        failures = sum(1 for r in self._results if not r)
        return failures / len(self._results)

    @property
    def stats(self) -> dict:
        """Current breaker statistics."""
        return {
            "name": self.name,
            "state": self.state.value,
            "consecutive_failures": self._consecutive_failures,
            "error_rate": round(self.error_rate, 4),
            "window_size": len(self._results),
            "trip_count": self._trip_count,
            "last_failure_time": self._last_failure_time,
            "cooldown_seconds": self.cooldown_seconds,
        }

    # ── Internals ────────────────────────────────────────────────────────

    def _do_trip(self, reason: str) -> None:
        """Transition to OPEN and fire the on_trip callback."""
        prev = self.state
        self.state = self.State.OPEN
        self._trip_count += 1
        self._last_failure_time = time.time()
        logger.warning(
            "CircuitBreaker[%s]: %s → OPEN (reason: %s, trip #%d)",
            self.name, prev.value, reason, self._trip_count,
        )
        if self.on_trip is not None:
            try:
                self.on_trip(self.name, reason)
            except Exception:
                logger.exception("on_trip callback failed for %s", self.name)

    def _do_reset(self) -> None:
        """Transition to CLOSED and fire the on_reset callback."""
        prev = self.state
        self.state = self.State.CLOSED
        self._consecutive_failures = 0
        logger.info(
            "CircuitBreaker[%s]: %s → CLOSED", self.name, prev.value,
        )
        if self.on_reset is not None:
            try:
                self.on_reset(self.name)
            except Exception:
                logger.exception("on_reset callback failed for %s", self.name)

    def __repr__(self) -> str:
        return (
            f"CircuitBreaker({self.name!r}, state={self.state.value}, "
            f"errors={self.error_rate:.2%}, trips={self._trip_count})"
        )


